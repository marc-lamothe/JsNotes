// * Context vs Scope

// * Scope
// ?   It's function-based.
// ?   It's the variable environment of a function when it is invoked.
// ?   Refers to the visibility of variables.

// * Context
// ?   It's object-based.
// ?   It's the value of the 'this' keyword, which is a reference to the object that owns that
// ?     current executing code.
// ?   Most often determined by how a function in invoked with the value of 'this' keyword.

//      Context and Scope are so often confused, that it's good to keep these definitions in mind.
